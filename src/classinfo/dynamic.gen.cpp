void dynamic::Populate()
{
    ClientClass *cc = g_IBaseClient->GetAllClasses();
    while (cc)
    {
        std::string name(cc->GetName());
        if (classid_mapping.find(name) != classid_mapping.end())
            *classid_mapping[name] = cc->m_ClassID;
        cc                         = cc->m_pNext;
    }
}

